class PageBuilder:
    """Build complete HTML pages programmatically."""
    
    def __init__(self, title):
        self.title = title
        self.sections = []
    
    def add_section(self, heading, content):
        self.sections.append((heading, content))
    
    def build(self):
        sections_html = ""
        for heading, content in self.sections:
            sections_html += f"""
        <section>
            <h2>{heading}</h2>
            <p>{content}</p>
        </section>"""
        
        return f"""<!DOCTYPE html>
<html>
<head>
    <title>{self.title}</title>
    <style>
        body {{
            font-family: 'Segoe UI', Tahoma, sans-serif;
            line-height: 1.6;
            max-width: 900px;
            margin: 0 auto;
            padding: 20px;
            color: #333;
        }}
        h1 {{
            color: #2c3e50;
            text-align: center;
        }}
        section {{
            background: #f8f9fa;
            padding: 20px;
            margin: 20px 0;
            border-radius: 8px;
            border-left: 4px solid #3498db;
        }}
        h2 {{
            color: #3498db;
            margin-top: 0;
        }}
    </style>
</head>
<body>
    <h1>{self.title}</h1>
    {sections_html}
</body>
</html>"""

# Build a page
builder = PageBuilder("My Learning Journey")
builder.add_section("Getting Started", "I began learning Python basics.")
builder.add_section("First Project", "Built a calculator application.")
builder.add_section("Next Steps", "Planning to learn web development.")

complete_page = builder.build()
print("Complete page HTML generated!")
print(f"Total length: {len(complete_page)} characters")

