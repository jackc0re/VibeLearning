def generate_table(headers, rows):
    """Generate HTML table from Python data."""
    header_html = "".join(f"<th>{h}</th>" for h in headers)
    rows_html = ""
    for row in rows:
        row_cells = "".join(f"<td>{cell}</td>" for cell in row)
        rows_html += f"        <tr>{row_cells}</tr>\n"
    
    return f"""<table border="1">
    <thead>
        <tr>{header_html}</tr>
    </thead>
    <tbody>
{rows_html}    </tbody>
</table>"""

headers = ["Name", "Age", "City"]
people = [
    ["Alice", "25", "New York"],
    ["Bob", "30", "London"],
    ["Carol", "28", "Tokyo"]
]

print(generate_table(headers, people))

