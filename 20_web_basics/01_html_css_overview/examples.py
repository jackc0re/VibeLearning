def generate_list(items, ordered=False):
    """Generate HTML list from Python list."""
    tag = "ol" if ordered else "ul"
    items_html = "\n".join(f"        <li>{item}</li>" for item in items)
    return f"""<{tag}>
{items_html}
    </{tag}>"""

fruits = ["Apple", "Banana", "Cherry", "Date"]
print("Unordered List:")
print(generate_list(fruits))

steps = ["Preheat oven", "Mix ingredients", "Bake for 30 min"]
print("\nOrdered List:")
print(generate_list(steps, ordered=True))

