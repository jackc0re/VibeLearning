def make_tag(name, content, **attrs):
    """Create an HTML tag with attributes."""
    attr_str = ""
    for key, value in attrs.items():
        attr_str += f' {key}="{value}"'
    return f"<{name}{attr_str}>{content}</{name}>"

# Create elements
heading = make_tag("h1", "Hello World", class_="main-title")
paragraph = make_tag("p", "This is a paragraph with a ", class_="text")
link = make_tag("a", "link", href="https://example.com", target="_blank")

print(f"Heading: {heading}")
print(f"Paragraph: {paragraph}")
print(f"Link: {link}")

