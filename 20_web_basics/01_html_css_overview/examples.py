def generate_nav(items, active=None):
    """Generate a navigation menu."""
    links = []
    for name, url in items:
        class_attr = ' class="active"' if name == active else ""
        links.append(f'        <li><a href="{url}"{class_attr}>{name}</a></li>')
    
    links_html = "\n".join(links)
    
    return f"""<nav>
    <ul>
{links_html}
    </ul>
</nav>
<style>
    nav ul {{
        list-style: none;
        padding: 0;
        display: flex;
        gap: 20px;
        background: #333;
        padding: 15px;
    }}
    nav a {{
        color: white;
        text-decoration: none;
        padding: 5px 15px;
        border-radius: 4px;
        transition: background 0.3s;
    }}
    nav a:hover {{
        background: #555;
    }}
    nav a.active {{
        background: #007bff;
    }}
</style>"""

nav_items = [
    ("Home", "/"),
    ("About", "/about"),
    ("Services", "/services"),
    ("Contact", "/contact")
]

print(generate_nav(nav_items, active="Home"))

